public class Kalkulator {
    public double wynik;
    public double dodawanie(double a, double b){
        return wynik = a + b;
    }
    public double odejmij(double a, double b){
        return wynik = a - b;
    }
    public double pomnoz(double a, double b){
        return wynik = a * b;
    }
    public double podziel(double a, double b){
        return wynik = a / b;
    }
}
